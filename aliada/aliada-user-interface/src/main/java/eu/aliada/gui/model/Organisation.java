package eu.aliada.gui.model;

/**
 * @author iosa
 * @since 1.0
 */
public class Organisation {
    private String name;
    private String path;
    private String uriDomain;
    private String uriResource;
    private String logo;
    private String catalogUrl;
    /**
     * @return Returns the name.
     * @exception
     * @since 1.0
     */
    public String getName() {
        return name;
    }
    /**
     * @param name The name to set.
     * @exception
     * @since 1.0
     */
    public void setName(String name) {
        this.name = name;
    }
    /**
     * @return Returns the path.
     * @exception
     * @since 1.0
     */
    public String getPath() {
        return path;
    }
    /**
     * @param path The path to set.
     * @exception
     * @since 1.0
     */
    public void setPath(String path) {
        this.path = path;
    }
    /**
     * @return Returns the uriDomain.
     * @exception
     * @since 1.0
     */
    public String getUriDomain() {
        return uriDomain;
    }
    /**
     * @param uriDomain The uriDomain to set.
     * @exception
     * @since 1.0
     */
    public void setUriDomain(String uriDomain) {
        this.uriDomain = uriDomain;
    }
    /**
     * @return Returns the uriResource.
     * @exception
     * @since 1.0
     */
    public String getUriResource() {
        return uriResource;
    }
    /**
     * @param uriResource The uriResource to set.
     * @exception
     * @since 1.0
     */
    public void setUriResource(String uriResource) {
        this.uriResource = uriResource;
    }
    /**
     * @return Returns the logo.
     * @exception
     * @since 1.0
     */
    public String getLogo() {
        return logo;
    }
    /**
     * @param logo The logo to set.
     * @exception
     * @since 1.0
     */
    public void setLogo(String logo) {
        this.logo = logo;
    }
    /**
     * @return Returns the catalogUrl.
     * @exception
     * @since 1.0
     */
    public String getCatalogUrl() {
        return catalogUrl;
    }
    /**
     * @param catalogUrl The catalogUrl to set.
     * @exception
     * @since 1.0
     */
    public void setCatalogUrl(String catalogUrl) {
        this.catalogUrl = catalogUrl;
    }
    
    

}
